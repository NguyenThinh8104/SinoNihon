package model;

public class AIQuestion {
    private String question;
    private String a;
    private String b;
    private String c;
    private String d;
    private String answer;
    private String explanation;

    public String getQuestion() {
        return question;
    }

    public String getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public String getC() {
        return c;
    }

    public String getD() {
        return d;
    }

    public String getAnswer() {
        return answer;
    }

    public String getExplanation() {
        return explanation;
    }
}
